START_COORDS = (0, 0)

# Assume the map is always the same width as the first line
MAP_WIDTH = len(input[0])

MAP_LENGTH = len(input)

def check_for_trees(map, coordinates):
    """Returns True if a given set of coordinates in a map is a tree"""
    y = coordinates[0]
    x = coordinates[1]
    return map[y][x] == "#"

def move_toboggan(origin_coords, move_x, move_y, map_width):
    """Returns new coordinates after moving the passed (x, y) values. If x goes over the passed map width, wrap back to the start"""
    return (origin_coords[0] + move_y, (origin_coords[1] + move_x) % map_width)

def go_tobogganing(map, starting_coords, move_x, move_y):
    trees_hit = 0
    coords = starting_coords
    if check_for_trees(map, starting_coords):
        raise Exception("Can't start on a tree, bad input file")
    while coords[0] < MAP_LENGTH:
        if check_for_trees(map, coords):
            trees_hit += 1
        coords = move_toboggan(coords, move_x, move_y, MAP_WIDTH)
    return trees_hit

# Part one
print(go_tobogganing(input, START_COORDS, 3, 1))

# Part two
run_one = go_tobogganing(input, START_COORDS, 1, 1)
run_two = go_tobogganing(input, START_COORDS, 3, 1)
run_three = go_tobogganing(input, START_COORDS, 5, 1)
run_four = go_tobogganing(input, START_COORDS, 7, 1)
run_five = go_tobogganing(input, START_COORDS, 1, 2)
print(run_one * run_two * run_three * run_four * run_five)
